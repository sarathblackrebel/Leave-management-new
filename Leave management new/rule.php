            <h2>LEAVE RULES</h2>
            <br>1.Introduction</h3><br>
            <p>
        Leave is a provision to stay away from work for genuine reasons with
        prior approval of the authorities. It may be granted for a casual purpose or a
        planned activity, on medical grounds or in extra-ordinary conditions.
         Leave cannot be claimed as a matter of right. Accordingly, leave rules
        and norms have been categorized under various heads. Vacation and leave are
        governed by a set of rules and norms as laid down by the Government of India.
        The following leave rules and norms give details about the different types of
        leave and how they can be availed of. Certain rules are common to both faculty
        and non-faculty of the Institute. Certain rules and norms have been stipulated
        specifically with reference to faculty of the Institute. These rules should be used
        for the better understanding and utilization of the leave rules and norms and are
        only complementary and explanatory in nature and in no way meant to
        supercede the leave provisions of the Government of India. </p>
        <br>2.General Principles Regarding Grant Of Leave <br>
        <br> &nbsp&nbsp&nbsp&nbsp&nbsp2.1 Applicability <br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe provisions contained in these rules shall apply to all employees of the
          ukf college of engineering and technology,paripally. </p>
         &nbsp&nbsp&nbsp&nbsp&nbsp2.2 Right to leave <br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspi. Leave cannot be claimed as a matter of right and the leave sanctioning
        authority may refuse or revoke leave of any kind.</p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspii. Leave sanctioning authority cannot alter the kind of leave due and
        applied for.</p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspiii. Leave will not be granted to staff under suspension.</p>
         &nbsp&nbsp&nbsp&nbsp&nbsp2.3 Authority empowered to sanction Leave<br>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspi. Applications for leave shall be addressed to the Board by the Director
        and to the Director/Registrar by the other members of staff. </p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspii. Leave may be sanctioned by the Director or by a member of staff to
        whom the power has been delegated by the Director.</p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspiii. The Board may sanction leave other than Casual Leave to the Director.
        The Director can avail himself of Casual Leave on his own authority.</p>
         &nbsp&nbsp&nbsp&nbsp&nbsp2.4 Commencement and termination of leave<br>
         <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspi. Leave ordinarily begins from the date on which leave as such is actually
        availed of and ends on the day preceding the date on which duty is
        resumed. </p>
         <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspii. Saturdays, Sundays, Restricted Holidays and other Holidays or the
        vacation may be prefixed as well as suffixed to any leave, subject to any limit of absence on leave prescribed under each &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspcategory of leave. </p>
        <br>3.Kinds of Leave <br>
        <br> &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp3.1. Casual Leave. (CL)
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspi. Casual Leave is not earned by duty. A staff on CL is not treated as absent from duty. CL cannot be claimed as of right and is subject to a
        maximum of 8 days in a calendar year. In addition each </p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspemployee will also be allowed to avail himself/herself of any two holidays from the list
        of Restricted holidays declared by the Government of India. </p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspii. CL can be combined with Special Casual Leave/Vacation/Restricted
        Holidays but not with any other kind of leave. </p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspiii. Saturdays, Sundays, restricted holiday and holidays, whether
        intervening, prefixed or suffixed, shall not be counted as Casual Leave.</p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspiv. CL should not be granted for more than 5 days at any time, except under
        special circumstances.</p>
         <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspv. CL can be taken for half a day also.</p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspvi. LTC can also be availed during CL.</p>
         <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspvii. CL cannot be combined with joining time.</p>
         <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspviii. Officials joining during the middle of a year may avail of CL
        proportionately or to the full extent at the discretion of the competent
        authority.</p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspix. Half-day’s CL should be debited to the CL account for each late
        attendance. However, late attendance upto one hour for not more than two occasions in a month can be condoned by the  &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspcompetent authority, if convinced that it is due to unavoidable reasons.</p>
        <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspx. Employees who have got only half day’s leave at credit when applying
        for half-day CL for the afternoon of a day should ensure that they attend office the next day since CL can not be combined &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspwith EL. However, if
        due to sickness other compelling grounds he/she is not able to attend the next day combining with EL can be permitted as an exception.</p>
